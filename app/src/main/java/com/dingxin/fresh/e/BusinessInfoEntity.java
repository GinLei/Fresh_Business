package com.dingxin.fresh.e;

public class BusinessInfoEntity {


    private String market_shop_name;
    private String realname;
    private String mobile;
    private String tab;
    private String bazaar_name;
    private String market_shop_content;
    private String is_upload;
    private String booth_image;

    public void setMarket_shop_name(String market_shop_name) {
        this.market_shop_name = market_shop_name;
    }

    public String getMarket_shop_name() {
        return market_shop_name;
    }

    public String getRealname() {
        return realname;
    }

    public void setRealname(String realname) {
        this.realname = realname;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getMobile() {
        return mobile;
    }

    public void setTab(String tab) {
        this.tab = tab;
    }

    public String getTab() {
        return tab;
    }

    public void setBazaar_name(String bazaar_name) {
        this.bazaar_name = bazaar_name;
    }

    public String getBazaar_name() {
        return bazaar_name;
    }

    public void setMarket_shop_content(String market_shop_content) {
        this.market_shop_content = market_shop_content;
    }

    public String getMarket_shop_content() {
        return market_shop_content;
    }


    public String getIs_upload() {
        return is_upload;
    }

    public void setIs_upload(String is_upload) {
        this.is_upload = is_upload;
    }

    public void setBooth_image(String booth_image) {
        this.booth_image = booth_image;
    }

    public String getBooth_image() {
        return booth_image;
    }


}
